/**
 * COREVIA Notification Orchestrator
 * 
 * Single source of truth for all notification channels across the system.
 * Every service registers its channels here. Admins toggle channels on/off.
 * When a notification is emitted, the orchestrator checks:
 *   1. Is the channel globally enabled?
 *   2. Does the user have a preference override?
 *   3. What delivery methods are configured?
 * Then routes the notification to the correct tables/transports.
 */

