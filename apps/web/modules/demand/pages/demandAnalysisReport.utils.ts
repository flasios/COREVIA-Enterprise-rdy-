export function hasDemandResubmission(
  workflowHistory?: Array<{ newStatus?: string | null; reason?: string | null; correction?: unknown }> | null,
): boolean {
  return Boolean(workflowHistory?.some((entry) => {
    if (entry.newStatus !== "generated") return false;
    if ("correction" in entry) return true;
    return /correct|resubmit|updated/i.test(entry.reason || "");
  }));
}

export function resolveDemandDisplayWorkflowStatus(
  workflowStatus?: string | null,
  workflowHistory?: Array<{ newStatus?: string | null; reason?: string | null; correction?: unknown }> | null,
): string {
  const status = workflowStatus || "generated";
  if (status === "generated" && hasDemandResubmission(workflowHistory)) {
    return "resubmitted";
  }
  return status;
}
