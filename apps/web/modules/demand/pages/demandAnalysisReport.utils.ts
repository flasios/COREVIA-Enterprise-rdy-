export function getBrainStatus(workflowStatus?: string): BrainStatus {
  if (workflowStatus === "pending_approval" || workflowStatus === "validation") {
    return {
      label: "Pending Approval",
      badgeClass: "bg-blue-500/10 text-blue-600 border-blue-500/20",
      nextGate: "PMO Director approval",
    };
  }

  if (workflowStatus === "resubmitted") {
    return {
      label: "Resubmitted",
      badgeClass: "bg-cyan-500/10 text-cyan-600 border-cyan-500/20",
      nextGate: "Human acknowledgement",
    };
  }

  if (workflowStatus === "rejected") {
    return {
      label: "Blocked",
      badgeClass: "bg-red-500/10 text-red-600 border-red-500/20",
      nextGate: "Revision or withdrawal",
    };
  }

  if (workflowStatus === "requires_more_info" || workflowStatus === "deferred") {
    return {
      label: "Needs Info",
      badgeClass: "bg-amber-500/10 text-amber-600 border-amber-500/20",
      nextGate: "Clarification",
    };
  }

  if (
    workflowStatus === "acknowledged" ||
    workflowStatus === "approved" ||
    workflowStatus === "manager_approved" ||
    workflowStatus === "pending_conversion" ||
    workflowStatus === "converted" ||
    workflowStatus === "completed"
  ) {
    return {
      label: "Approved",
      badgeClass: "bg-emerald-500/10 text-emerald-600 border-emerald-500/20",
      nextGate: "Execution",
    };
  }

  if (workflowStatus === "manager_approval") {
    return {
      label: "Final Approval",
      badgeClass: "bg-blue-500/10 text-blue-600 border-blue-500/20",
      nextGate: "Director signoff",
    };
  }

  if (workflowStatus === "initially_approved") {
    return {
      label: "Pre-Approved",
      badgeClass: "bg-indigo-500/10 text-indigo-600 border-indigo-500/20",
      nextGate: "Director review",
    };
  }

  if (workflowStatus === "meeting_scheduled") {
    return {
      label: "Review Scheduled",
      badgeClass: "bg-purple-500/10 text-purple-600 border-purple-500/20",
      nextGate: "Panel review",
    };
  }

  if (workflowStatus === "under_review") {
    return {
      label: "In Review",
      badgeClass: "bg-yellow-500/10 text-yellow-700 border-yellow-500/20",
      nextGate: "Validation",
    };
  }

  return {
    label: "Generated",
    badgeClass: "bg-slate-500/10 text-slate-600 border-slate-500/20",
    nextGate: "Review",
  };
}

